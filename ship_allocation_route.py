def allocate_orders(trucks, orders):

    # Calculate remaining capacity for every truck
    for truck in trucks:
        used_capacity = sum(
            order["weight"]
            for order in truck["existing_orders"]
        )

        truck["remaining_capacity"] = (
            truck["capacity"] - used_capacity
        )

        truck["assigned_orders"] = []

    # Heaviest orders allocated first
    orders = sorted(
        orders,
        key=lambda x: x["weight"],
        reverse=True
    )

    unallocated_orders = []

    # Allocate orders
    for order in orders:

        allocated = False

        for truck in trucks:

            if truck["remaining_capacity"] >= order["weight"]:

                truck["assigned_orders"].append(order)

                truck["remaining_capacity"] -= order["weight"]

                allocated = True
                break

        if not allocated:
            unallocated_orders.append(order)

    return trucks, unallocated_orders

