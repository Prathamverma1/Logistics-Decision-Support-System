def haversine(city1, city2):
    lat1, lon1 = city_locations[city1.lower()]
    lat2, lon2 = city_locations[city2.lower()]

    R = 6371  # Earth radius in km

    dlat = radians(lat2 - lat1)
    dlon = radians(lon2 - lon1)

    a = (
        sin(dlat / 2) ** 2
        + cos(radians(lat1))
        * cos(radians(lat2))
        * sin(dlon / 2) ** 2
    )

    c = 2 * atan2(sqrt(a), sqrt(1 - a))

    return R * c


def optimize_routes(trucks, start_city="kanpur"):

    truck_routes = {}

    for truck in trucks:

        # Start from warehouse
        route = [start_city.title()]

        # Existing orders must be delivered first
        existing_destinations = [
            order["destination"].lower()
            for order in truck.get("existing_orders", [])
        ]

        route.extend([city.title() for city in existing_destinations])

        current_city = (
            existing_destinations[-1]
            if existing_destinations
            else start_city
        )

        # New assigned orders
        remaining_destinations = [
            order["destination"].lower()
            for order in truck.get("assigned_orders", [])
        ]

        # Find nearest destination repeatedly
        while remaining_destinations:

            nearest_city = min(
                remaining_destinations,
                key=lambda city:
                haversine(current_city, city)
            )

            route.append(nearest_city.title())

            remaining_destinations.remove(nearest_city)

            current_city = nearest_city

        # Return to warehouse
        route.append(start_city.title())

        truck_routes[truck["truck_id"]] = route

    return truck_routes

