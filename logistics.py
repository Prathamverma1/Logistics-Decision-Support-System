def scenario_demand(model_weight,forecast_weight,df_log,per,total_trucks,average_weight_cap):
  today = pd.Timestamp.today().normalize()
  month_start = today.replace(day=1)
  month_end = today + pd.offsets.MonthEnd(0)
  days_left=month_end.day-today.day
  print(days_left)
  total_pred_weight=0
  if days_left>0:
     df=forecast_weight(model_weight,df_log.copy(),days_left)
     total_pred_weight=df['predicted_weight_kg'].sum()

  df_log=df_log.sort_values('order_date').copy()
  df_log=df_log[(df_log['order_date']>=month_start) & (df_log['order_date']<=today)]

  total_prev_weight=df_log['total_weight'].sum()

  over_all_weight=(total_pred_weight+total_prev_weight)/30

  over_all_weight=over_all_weight+(over_all_weight*(per/100))

  if over_all_weight<=(average_weight_cap*total_trucks):
    print("0 trucks needed")

  else:
    extra_trucks=np.ceil((over_all_weight-(average_weight_cap*total_trucks))/average_weight_cap)
    print("extra trucks needed",extra_trucks)

