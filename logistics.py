def forecast_shipments(model, df_model_1, n_days):

    history = df_model_1.sort_values(
        'order_date'
        )

    forecasts = []
    recent_date=pd.Timestamp.today().normalize()
    start_date = recent_date - pd.Timedelta(days=30)
    history=history[(history['order_date']>=start_date) & (history['order_date']<=recent_date)]

    for i in range(n_days):


        next_date = (
            recent_date
            + pd.Timedelta(days=1)
        )


        month = next_date.month
        week_of_year = next_date.isocalendar().week
        quarter = next_date.quarter

        is_weekend = int(
            next_date.dayofweek >= 5
        )

        is_month_start = int(
            next_date.is_month_start
        )

        is_month_end = int(
            next_date.is_month_end
        )


        lag_1 = history.iloc[-1]['number_of_shipments']
        lag_7 = history.iloc[-7]['number_of_shipments']
        lag_14 = history.iloc[-14]['number_of_shipments']
        lag_28 = history.iloc[-28]['number_of_shipments']


        rolling_mean_7 = (
            history.tail(7)
            ['number_of_shipments']
            .mean()
        )

        rolling_mean_14 = (
            history.tail(14)
            ['number_of_shipments']
            .mean()
        )

        rolling_mean_28 = (
            history.tail(28)
            ['number_of_shipments']
            .mean()
        )

        rolling_std_7 = (
            history.tail(7)
            ['number_of_shipments']
            .std()
        )

        ewm_7 = (
            history['number_of_shipments']
            .ewm(
                span=7,
                adjust=False
            )
            .mean()
            .iloc[-1]
        )



        X_future = pd.DataFrame({
            'month': [month],
            'week_of_year': [week_of_year],
            'quarter': [quarter],
            'is_weekend': [is_weekend],
            'is_month_start': [is_month_start],
            'is_month_end': [is_month_end],
            'lag_1': [lag_1],
            'lag_7': [lag_7],
            'lag_14': [lag_14],
            'lag_28': [lag_28],
            'rolling_mean_7': [rolling_mean_7],
            'rolling_mean_14': [rolling_mean_14],
            'rolling_mean_28': [rolling_mean_28],
            'rolling_std_7': [rolling_std_7],
            'ewm_7': [ewm_7]
        })



        prediction = model.predict(
            X_future
        )[0]


        prediction = max(
            0,
            round(prediction)
        )


        forecasts.append({
            'forecast_date': next_date,
            'predicted_shipments': prediction
        })



        new_row = {
            'order_date': next_date,
            'month': month,
            'week_of_year': week_of_year,
            'quarter': quarter,
            'is_weekend': is_weekend,
            'is_month_start': is_month_start,
            'is_month_end': is_month_end,
            'number_of_shipments': prediction,
            'lag_1': lag_1,
            'lag_7': lag_7,
            'lag_14': lag_14,
            'lag_28': lag_28,
            'rolling_mean_7': rolling_mean_7,
            'rolling_mean_14': rolling_mean_14,
            'rolling_mean_28': rolling_mean_28,

            'rolling_std_7': rolling_std_7,

            'ewm_7': ewm_7
        }

        history = pd.concat(
            [
                history,
                pd.DataFrame([new_row])
            ],
            ignore_index=True
        )
        recent_date=next_date


    forecast_df = pd.DataFrame(
        forecasts
    )

    return forecast_df

